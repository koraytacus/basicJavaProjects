package theMultiplicationTable;

public class multiplicationTable {

	public static void main(String[] args) {
		
		
		int startNumb = 1;
		while (startNumb<=10) {
			
			
			for(int a = 1;a<=10;a++) {
				
				System.out.println(startNumb + " x " + a + " = " + startNumb*a);
				
			}
			
				startNumb++;
				System.out.println("---------------------------------------------------------------------------");
			
			
			
			
			
			
			
		}
		
		
		
		
		
		
		
		

	}

}
